		var product_item = $('.product_title').text();
		$("#detail").val(product_item);
		
		$('.modal-title').html('Để lại lời nhắn cho chúng tôi');
		$('#dat-hang').html('Lời Nhắn');
		$('#type-request').val(0);
		
		showHide();
		
		$('.15-phut').hide();
	});
	$(".goi-lai").click(function(){
		var product_item = $('.product_title').text();
		$("#detail").val(product_item);
		
		$('.modal-title').html('Dịch vụ hỗ trợ “Tư vấn miễn phí"');
		$('#dat-hang').html('Yêu cầu Gọi Lại');
		$('#type-request').val(0);
		
		showHide();
	});

	function showHide(){
		$("#alert .alert-danger").html('');
		$("#alert .alert-success").html('');
		$("#alert .alert-success").hide();
		$("#alert .alert-danger").hide();
		$('.15-phut').show();
	}

	$("#dat-hang").click(function(){
		$("#alert .alert-success").hide();
		$("#alert .alert-danger").hide();
		var phone = $("#phone").val();
		var name = $("#name").val();
		var address = $("#address").val();
		var detail = $("#detail").val();
		var type= $('#type-request').val();
		$("#dat-hang").prop( "disabled", true );
		
		$.ajax({
		  method: "POST",
		  url: "https://m.zsofa.vn/index.php/site/NewBill",
		  data: { name: name, phone: phone, address : address, detail: detail, type: type},
		  success: function(data){	  
			  $("#dat-hang").prop( "disabled", false );  
			  data = JSON.parse(data)
			  if(data.code == 200){
				  $("#alert .alert-success").show();
				  $("#alert .alert-success").html(data.message);
			  }else{
				  $("#alert .alert-danger").show()
				  $("#alert .alert-danger").html(data.message);
			  }
		  }
		})
	});
	/*** remove <p> *****/
	jQuery('.panel-group .panel-default br').remove(); 
